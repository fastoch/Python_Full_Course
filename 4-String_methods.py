# Length method
name = "fastoch"
print(len(name))

# find() - returns the index (position) of a character within a string
print(name.find("s")) # first position = index 0

# capitalize () - first letter is uppercase
print(name.capitalize())

# upper () - all letters are uppercase
print(name.upper())

# lower ()
print(name.lower())

# isdigit()
print(name.isdigit())

# isalpha() - does my string contain only alphabetical letters?
print(name.isalpha())

# count()
print(name.count("o"))

# replace()
print(name.replace("o","i"))

